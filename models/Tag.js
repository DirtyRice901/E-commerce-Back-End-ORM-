const { Model, DataTypes } = require('sequelize');

const sequelize = require('../config/connection.js');

class Tag extends Model {}

Tag.init(
  {
  /////////////////// define columns/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    id: {
      ////////Set data types as integer/////////////////////////////////////////////////////////////////////////////////////////////////////////
      type: DataTypes.INTEGER,
      ////////Set Non Null values///////////////////////////////////////////////////////////////////////////////////////////////////////////////
      allowNull: false,
      ////////Set as the primary key////////////////////////////////////////////////////////////////////////////////////////////////////////////
      primaryKey: true,
      ////////Set to auto incremental///////////////////////////////////////////////////////////////////////////////////////////////////////////
      autoIncrement: true,
    },
    tag_name: {
      ////////Set data types as integer/////////////////////////////////////////////////////////////////////////////////////////////////////////
      type: DataTypes.STRING,
    },
  },
  {
    sequelize,
    timestamps: false,
    freezeTableName: true,
    underscored: true,
    modelName: 'tag',
  }
);

module.exports = Tag;
