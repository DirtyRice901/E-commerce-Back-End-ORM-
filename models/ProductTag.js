const { Model, DataTypes } = require('sequelize');

const sequelize = require('../config/connection');

class ProductTag extends Model {}

ProductTag.init(
  {
  //////////define columns///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    id: {
      //////////Set the data type as integer/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
      type: DataTypes.INTEGER,
      //////////Set Non Null values//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
      allowNull: false,
      //////////Set as the primary key///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
      primaryKey: true,
      //////////Set to auto incremental//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
      autoIncrement: true,
    },
    tag_id: {
      //////////Set data type as interger////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
      type: DataTypes.INTEGER,
      references: {
        ///////Reference the id key from the product model * foreign key *//////////////////////////////////////////////////////////////////////
        model: "product",
        key: "id",
      },
    },
  },
  {
    sequelize,
    timestamps: false,
    freezeTableName: true,
    underscored: true,
    modelName: 'product_tag',
  }
);

module.exports = ProductTag;
